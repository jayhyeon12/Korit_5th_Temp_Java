package com.study.ch04;

public class Operation06 {
    public static void main(String[] args) {
        /* 증감연산자

        */
        int i = 0;
//        i++;  (i += 1;)
        System.out.println(i++);
        System.out.println(i++);

        System.out.println(++i);
        System.out.println(++i);

        System.out.println(i--);
        System.out.println(--i);

    }
}
